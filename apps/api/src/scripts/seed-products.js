function productImageUrl(name) {
  const imageName = name
    .toLowerCase()
    .replace(/\bpro max\b/g, "promax")
    .replace(/[()]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "");

  return `/assets/images/${imageName}.png`;
}

function primaryImage(name) {
  return [
    {
      url: productImageUrl(name),
      alt: name,
      sortOrder: 0,
    },
  ];
}

